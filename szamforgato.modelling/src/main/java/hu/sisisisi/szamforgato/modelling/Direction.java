package hu.sisisisi.szamforgato.modelling;

/**
 * Két dimenziós sík beli irányokat reprezentál.
 */
public enum Direction
{
    /** Az az irány, mely a kisebb X koordináta felé található. */
    Left,
    /** Az az irány, mely a nagyobb X koordináta felé található. */
    Right,
    /** Az az irány, mely a kisebb Y koordináta felé található. */
    Up,
    /** Az az irány, mely a nagyobb Y koordináta felé található. */
    Down
}
